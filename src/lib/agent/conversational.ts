export const CONVERSATIONAL_TOOLS = ['web_search', 'search_knowledge'];

export function isConversationalQuery(content: string): boolean {
  const pattern = /帮我|帮写|帮我写|帮我做|给我写|给我做|写一|做一个|生成一|创作|写篇|写个|做个|搞一篇|整一篇/;
  return pattern.test(content);
}
