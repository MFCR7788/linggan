## 病毒传播 6 因子评估
收到热点事件后，对以下 6 个维度打分（1-5）：
1. **情绪强度**：事件引发愤怒/感动/震惊/欢乐的程度
2. **身份关联**：与用户自我认同的关联度（"这说的就是我"）
3. **社交货币**：分享这个内容能让分享者显得怎样（有见识/有趣/有态度）
4. **实用价值**：内容包含可操作的信息或知识
5. **故事性**：事件本身的叙事张力、反转、冲突
6. **参与门槛**：用户参与讨论的难易度（越低传播越广）

## 内容角度矩阵（5 个维度 × 事件）
| 角度类型 | 说明 | 适合平台 |
|---------|------|---------|
