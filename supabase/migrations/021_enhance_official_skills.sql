## 核心拍摄法则

### 180 度法则（对话/多人场景必须遵守）
- 在两个对话人物之间画一条虚拟的 180° 轴线
- 所有机位必须在这条线的同一侧
- 违反 → 人物视线方向混乱，观众困惑
- 跨线方式：用中性镜头（正对轴线）过渡，或用人物移动自然跨越

### 动作衔接（Match on Action）
- 前一个镜头中开始的动作，在下一个镜头中从不同角度继续
- 例：镜头A（中景）人物伸手开门 → 镜头B（特写）手转动门把手
- 剪接点选在动作进行中，不要在动作开始前或结束后

### 视线匹配（Eyeline Match）
- 前一个镜头人物看向画外某处 → 下一个镜头展示他看的内容
- 视线方向必须一致（左看 → 目标物在画面右侧）

### 30 度规则
- 同一主体的两个连续镜头，机位角度必须相差至少 30°
- 小于 30° → 画面像"跳了一下"，不流畅

## 构图法则
- **三分法**（Rule of Thirds）：主体放在交叉点上，视线方向留白（Lead Room）
- **头顶留白**（Headroom）：特写少留、全景适当多留，但不要"顶天"
- **深度感**：前景 + 中景 + 背景三层制造空间感
- **引导线**：利用环境线条引导视线到主体

